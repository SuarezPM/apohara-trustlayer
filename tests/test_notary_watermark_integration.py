def _biased_green_tokens(key: bytes, n: int, vocab_size: int = 1000, gamma: float = 0.25) -> list[int]:
    """Generate tokens that always fall in the green list (deterministic)."""
    green_size = max(1, int(gamma * vocab_size))
    out: list[int] = []
    for pos in range(n):
        seed_material = key + pos.to_bytes(8, "little", signed=False)
        d = hashlib.blake2b(seed_material, digest_size=32).digest()
        seed = int.from_bytes(d[:4], "little", signed=False)
        green = [(seed + i * 0x9E3779B1) % max(1, vocab_size) for i in range(green_size)]
        # Pick deterministically (use the first green token) so the
        # output is reproducible across runs.
        out.append(min(green))
    return out


